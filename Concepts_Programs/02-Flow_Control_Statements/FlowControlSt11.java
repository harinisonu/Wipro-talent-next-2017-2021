/*******************************************************************************
 
11)

Write a program to print even numbers between 23 and 57, each number should be printed in a
separate row.
Topics Covered: For Loop
 
 *******************************************************************************/
public class FlowControlSt11
{
	public static void main(String[] args) 
	{		
		for(int i = 24; i < 57; )//looping 10 times
		{
			System.out.println(i);//print numbers from 1 to 10
			i = i+2;
		}		
		
	}
}
